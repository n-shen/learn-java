package dev.shen.java.basic.introduction.helloworld;

/**
 * The most basic Hello World example in Java.
 * This class demonstrates:
 * - Creating a class
 * - The main method (entry point)
 * - Printing to the console
 */
public class HelloWorld {

    /**
     * Main method - the entry point for Java applications.
     * The JVM looks for this specific method signature to start execution.
     *
     * @param args Command line arguments (not used in this example)
     */
    public static void main(String[] args) {
        // Print a message to the console
        System.out.println("Hello, World!");
    }
}
